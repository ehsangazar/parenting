/**
 * Regenerates src/components/icons/iconMap.ts from every *.svg in assets/.
 * Run: node scripts/generate-icon-map.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ASSETS = path.join(__dirname, '../src/components/icons/assets');
const OUT = path.join(__dirname, '../src/components/icons/iconMap.ts');

function importId(stem) {
  const parts = stem.split(/[_-]+/).filter(Boolean);
  return (
    'icon' +
    parts.map((p) => (p.length ? p.charAt(0).toUpperCase() + p.slice(1) : '')).join('')
  );
}

const files = fs.readdirSync(ASSETS).filter((f) => f.endsWith('.svg')).sort();
const usedIds = new Map();
const imports = [];
const entries = [];

for (const f of files) {
  const stem = f.slice(0, -4);
  let id = importId(stem);
  if (!id || id === 'icon') {
    id = 'iconAsset' + stem.length;
  }
  let uniqueId = id;
  let n = 2;
  while (usedIds.has(uniqueId)) {
    uniqueId = `${id}${n++}`;
  }
  usedIds.set(uniqueId, stem);
  imports.push(`import ${uniqueId} from './assets/${f}?url';`);
  entries.push(`  '${stem}': ${uniqueId},`);
}

const content =
  '/** Auto-generated by `node scripts/generate-icon-map.mjs`. Do not edit manually. */\n\n' +
  imports.join('\n') +
  '\n\nexport const iconUrls = {\n' +
  entries.join('\n') +
  '\n} as const;\n\nexport type IconName = keyof typeof iconUrls;\n';

fs.writeFileSync(OUT, content);
console.log(`Wrote ${files.length} icons to src/components/icons/iconMap.ts`);
